#include "push_swap.h"

void	swap(t_stack *stack)
{
	int	tmp;

	while (!stack || !stack->next)
		return ;
	tmp = stack->value;
	stack->value = stack->next->value;
	stack->next->value = tmp;
	tmp = stack->index;
	stack->index = stack->next->index;
	stack->next->index = tmp;
}

void	sa(t_stack **stack)
{
	swap(*stack);
	ft_putstr("sa\n");
}
