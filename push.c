#include "push_swap.h"

void	push(t_stack **src, t_stack **dest)
{
	t_stack	*tmp;
	t_stack	*tmp2;

	while (*src == NULL)
		return ;
	if (!(*src)->next)
		tmp = NULL;
	else
		tmp = (*src)->next;
	tmp2 = (*src);
	tmp2->next = *dest;
	*dest = tmp2;
	*src = tmp;
}

void	pa(t_stack **stack_a, t_stack **stack_b)
{
	push(stack_b, stack_a);
	ft_putstr("pa\n");
}

void	pb(t_stack **stack_a, t_stack **stack_b)
{
	push(stack_a, stack_b);
	ft_putstr("pb\n");
}
